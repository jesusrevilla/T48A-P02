"""3. Create a NumPy array of the first 10 prime numbers."""
def first_10_primes():
    """
    Crea un arreglo de numpy con los diez primeros números primos.
    """
    primos = []
    num = 2
    while len(primos) < 10:
        es_primo = True
        for p in primos:
            if num % p == 0:
                es_primo = False
                break
            if p * p > num:
                break
        if es_primo:
            primos.append(num)
        num += 1
    return np.array(primos)


